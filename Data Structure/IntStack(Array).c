#include <stdio.h>
#include <stdlib.h>
#include "IntStack.h"
int main(void) {
	IntStack s;
	if (Initialize(&s, 64) == -1) {
		puts("스택 생성 실패.");
		return 1;
	}

	while (1) {
		int menu, x, key, idx;
		printf("현재 데이터 수 : %d / %d\n", Size(&s), Capacity(&s));
		printf("(1)푸시  (2)팝  (3)피크  (4)출력  (5)검색  (0)종료 : ");
		scanf_s("%d", &menu);

		if (menu == 0) break;
		switch (menu)
		{
		case 1: /* 푸시 */
			printf("데이터 : ");
			scanf_s("%d", &x);

			if (Push(&s, x) == -1)
				puts("오류 : 푸시에 실패하였습니다.");
			break;

		case 2: /* 팝 */
			if (Pop(&s, &x) == -1)
				puts("오류 : 팝에 실패하였습니다.");
			else
				printf("팝 데이터는 %d입니다.\n", x);
			break;

		case 3: /* 피크 */
			if(Peek(&s, &x) == -1)
				puts("오류 : 피크에 실패하였습니다.");
			else
				printf("피크 데이터는 %d입니다.\n", x);
			break;

		case 4: /* 출력 */
			Print(&s);
			break;

		case 5: /* 검색 */
			printf("검색할 데이터 입력 : ");
			scanf_s("%d", &key);

			idx = Search(&s, key);
			if (idx  == -1)
				printf("%d는 스택에 존재하지않습니다.\n", key);
			else {
				printf("검색성공!\n");
				printf("%d는 stk[%d]에 있습니다.\n", key, idx);
			}
		}
		putchar('\n');
	}
	Terminate(&s);
	return 0;
}